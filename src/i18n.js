const SUPPORTED_LANGS = ['en', 'it', 'fr', 'es', 'nl', 'de', 'pt'];

/**
 * Resolve the language to use for the card.
 * Priority: config.language → hass.locale.language → hass.language → 'en'.
 * Only supported languages are accepted.
 */
export function resolveLang(hass, config) {
  const candidates = [];
  if (config && typeof config.language === 'string') candidates.push(config.language);
  if (hass && hass.locale && hass.locale.language) candidates.push(hass.locale.language);
  if (hass && hass.language) candidates.push(hass.language);
  for (const c of candidates) {
    if (!c) continue;
    const code = String(c).toLowerCase().split('-')[0];
    if (SUPPORTED_LANGS.includes(code)) return code;
  }
  return 'en';
}

/**
 * Return the translation for the given key in the chosen language, with fallback
 * to English and then to the key itself. Supports {n}, {team}, etc. interpolation.
 */
export function t(key, lang, vars) {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  let str = dict[key];
  if (str === undefined) str = TRANSLATIONS.en[key];
  if (str === undefined) return key;
  if (vars) {
    Object.keys(vars).forEach(k => {
      str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
    });
  }
  return str;
}

export const SUPPORTED_LANGUAGES = SUPPORTED_LANGS;

// BCP-47 locale per language code used for Intl.DateTimeFormat
const LOCALE_MAP = {
  nl: 'nl-NL', de: 'de-DE', fr: 'fr-FR',
  it: 'it-IT', es: 'es-ES', pt: 'pt-PT',
  en: 'en-GB',
};

export function parseMatchDate(dateStr) {
  if (!dateStr || dateStr === 'N/A') return null;
  const m = String(dateStr).match(/^(\d{2})[-/](\d{2})[-/](\d{4})(?:\s+(\d{2}):(\d{2}))?/);
  if (m) {
    const date = new Date(+m[3], +m[2] - 1, +m[1], +(m[4] || 0), +(m[5] || 0));
    return Number.isFinite(date.getTime()) ? date : null;
  }
  // ISO 8601 fallback — e.g. H2H dates from ESPN: "2022-06-23T18:00Z"
  const iso = new Date(dateStr);
  return Number.isFinite(iso.getTime()) ? iso : null;
}

export function parseMatchTimestamp(dateStr) {
  const date = parseMatchDate(dateStr);
  return date ? date.getTime() : 0;
}

/**
 * Formats a sensor date string ("dd-mm-yyyy hh:mm") for display.
 * Today's matches → time only ("12:15").
 * Other dates → locale-aware short date + time ("8-9 12:15" for nl, "8/9 12:15" for en-GB, etc.)
 * @param {string} dateStr  Sensor date like "08-09-2025 12:15"
 * @param {string} lang     Language code from resolveLang()
 * @returns {string}
 */
export function formatMatchDate(dateStr, lang) {
  if (!dateStr) return '';
  const m = dateStr.match(/^(\d{2})-(\d{2})-(\d{4})\s+(\d{2}):(\d{2})$/);
  if (!m) return dateStr;
  const [, dd, mm, yyyy, hh, min] = m;
  const date = parseMatchDate(dateStr);
  if (!date) return dateStr;
  const now = new Date();
  const timeStr = `${hh}:${min}`;

  if (
    date.getDate() === now.getDate() &&
    date.getMonth() === now.getMonth() &&
    date.getFullYear() === now.getFullYear()
  ) return timeStr;

  const locale = LOCALE_MAP[lang] || 'en-GB';
  try {
    const datePart = new Intl.DateTimeFormat(locale, { day: 'numeric', month: 'numeric' }).format(date);
    return `${datePart} ${timeStr}`;
  } catch (_) {
    return `${+dd}/${+mm} ${timeStr}`;
  }
}

/**
 * Date-only display for past match results ("14 jun", "28 Jun", "14 giu").
 * Uses locale short month name, no time component.
 */
export function formatDateOnly(dateStr, lang) {
  if (!dateStr) return '';
  const date = parseMatchDate(dateStr);
  if (!date) return dateStr.split(' ')[0] || dateStr;
  const locale = LOCALE_MAP[lang] || 'en-GB';
  try {
    return new Intl.DateTimeFormat(locale, { day: 'numeric', month: 'short' }).format(date);
  } catch (_) {
    const m = dateStr.match(/^(\d{2})-(\d{2})-(\d{4})/);
    return m ? `${+m[1]}/${+m[2]}` : dateStr;
  }
}

/**
 * Full date+time for badge displays (always shows date, even for today).
 * nl-NL → "26-06-2026 01:00", en-GB → "26/06/2026 01:00", de-DE → "26.06.2026 01:00"
 */
export function formatMatchDateFull(dateStr, lang) {
  if (!dateStr) return '';
  const m = dateStr.match(/^(\d{2})-(\d{2})-(\d{4})\s+(\d{2}):(\d{2})$/);
  if (!m) return dateStr;
  const [, dd, mm, yyyy, hh, min] = m;
  const date = parseMatchDate(dateStr);
  if (!date) return dateStr;
  const locale = LOCALE_MAP[lang] || 'en-GB';
  try {
    const datePart = new Intl.DateTimeFormat(locale, { day: '2-digit', month: '2-digit', year: 'numeric' }).format(date);
    return `${datePart} ${hh}:${min}`;
  } catch (_) {
    return dateStr;
  }
}
